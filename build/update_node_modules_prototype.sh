#!/bin/bash
# This script generates the prototype file from the node_modules directory.
#
# Because pkgproto is so inflexible, this script is a bit of a hack to coerce
# desireable output from it.

DIR=`dirname $0`
cd $DIR;
(
    pkgproto ../node_modules=provisioner/node_modules                            \
      | perl -ne 'if ([split]->[2] !~ /\.o$/) { s/\w+ \w+$/root root/g; print }' \
      > node_modules_prototype
)
